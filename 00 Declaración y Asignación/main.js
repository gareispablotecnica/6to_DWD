/* 

    Declaración
    En JavaScript, pueden declarar variables usando:

        - var (forma antigua, ya no se recomienda)

        - let (forma moderna, permite reasignación)

        - const (forma moderna, no permite reasignación)


*/

// ---> Ejemplo: 
let Nombre = "Pablo"
let Edad=30

