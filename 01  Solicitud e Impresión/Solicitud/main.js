/*

    *prompt()
        Sirve para solicitar mediante un INPUT un dato al Usuario.

        prompt("Ingrese su Nombre: ")

        Captura datos tipo String

    * parseInt o parseFloat
        parseInt --> Convierte un String en un Entero
        -parseInt(prompt("Ingrese su Edad: "))
        
        parseFloat --> Convierte un String en un Decimal
        -parseFloat(prompt("Ingrese su Sueldo: "))
*/


// ---> Solicitamos el Ingreso del nombre del Usuario

let nombre =prompt("Ingrese su Nombre: ")

/*
    - nombre --> Variable que almacena el Dato ingresado por el Usuario

    - let ---> Asignacion de Variable

    - prompt ("Ingrese su Nombre: ") --> Solicitud por Pantalla

*/

